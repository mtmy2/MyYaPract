Программа не знает, какой сервер вы будете использовать, поэтому его нужно выбрать самостоятельно. Выберите PostgreSQL и нажмите Далее.
Вам нужно указать настройки соединения. Нужные параметры вы получили во время регистрации в сервисе проверок.
Хост — значение поля host из полученных параметров подключения.
База данных — значение поля dbname из полученных параметров подключения.
Порт — 6432.
Аутентификация — Database Native.
Пользователь — значение поля user из полученных параметров подключения.
Пароль — значение поля password из полученных параметров подключения.
Обязательно выберите пункт Сохранять пароль локально. Это позволит не вводить пароль заново при каждом входе.
Вы сформировали учётную запись базы — совокупность данных, которая нужна, чтобы пользователь мог войти в систему и получить доступ к своим инструментам и настройкам. Нажмите Готово и завершите настройку соединения.
Программа может попросить согласие на установку набора драйверов, которые понадобятся для создания соединения. Согласитесь на установку — без нужных драйверов DBeaver работать не будет.
Заодно напомним основной интерфейс. После завершения настройки соединения в окне навигатора должна появиться добавленная СУБД. Название базы данных будет выделено жирным шрифтом. Чтобы начать писать запрос, щёлкните по нему правой кнопкой мыши и выберите Редактор SQL (SQL Editor) → Открыть SQL-скрипт (Open SQL script).
Пункт Таблицы содержит доступные таблицы БД. Если вы нажмёте левой кнопкой мыши на этот пункт, то откроется вкладка Свойства. Она отражает структуру таблицы: поля, типы, ключи, индексы.
Вкладка Данные отражает данные таблицы, а вкладка Диаграмма позволяет посмотреть на ER-диаграмму таблиц и их связей в БД.
