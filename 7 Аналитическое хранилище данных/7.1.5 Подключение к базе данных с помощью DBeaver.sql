Подключение к базе данных с помощью DBeaver
DBeaver на вашем компьютере уже готов к работе, пора подключиться к новой базе данных. Любое подключение начинается с создания и настройки соединения — этим вы сейчас и займётесь. 
Прежде чем приступить к практике, вам нужно зарегистрироваться в сервисе проверок, чтобы он создал для вас необходимую среду — базу данных. Для этого нажмите Проверить в задании. В ответ система зарегистрирует вас в сервисе и в поле Результат выведет параметры для подключения к базе данных. Сохраните их — они понадобятся вам далее.
